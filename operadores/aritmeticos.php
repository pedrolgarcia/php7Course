<?php 

    $a = 10;
    $b = 2;

    echo $a + $b . "<br>"; //Soma
    echo $a - $b . "<br>"; //Subtração
    echo $a * $b . "<br>"; //Multiplicação
    echo $a / $b . "<br>"; //Divisão
    echo $a % $b . "<br>"; //Módulo
    echo $a ** $b . "<br>"; //Potência

    echo $a++; //Incremento
    echo $a--; //Decremento
    echo ++$a; //Pré-incremento, incrementa depois mostra
    echo --$a; //Pré-decremento , decrementa depois mostra

?>